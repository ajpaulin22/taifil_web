                <h6 class="text-center">What is Specified Skilled Worker Program (SSW)?</h6>
                <p >Specified Skilled Worker is a status of residence created to give foreign nationals more opportunities to work in Japan. There are two types of Specified Skilled Worker status of residence: type (i) and type (ii). The type (ii) status of residence requires more specialized skills than type (i). Source - www.ssw.go.jp</p>
                <br>
                <br>
                <p>
                    14 Employment Fields Covered by Specified Skilled Worker (i)
                </p>
                <ul class="offset-4">
                    <li>01 - Nursing Care</li>
                    <li>02 - Building Cleaning Management</li>
                    <li> 03 - Construction Industry (Jobs constructing houses, buildings, and other structures)</li>
                    <li>04 - Machine Parts and Tooling Industries</li>
                    <li>05 - Industrial Machinery Industry</li>
                    <li>06 - Electric, Electronics and Information Industries</li>
                    <li>07 - Ship building and Ship MAchinery Industries (Building Ships)</li>
                    <li>08 - Automobile and Repair Industries</li>
                    <li>09 - Aviation Industry</li>
                    <li>10 - Accommodation Industry</li>
                    <li>11 - Agriculture Industry</li>
                    <li>12 - Fishery and Agriculture Industries</li>
                    <li>13 - Food and Beverage Manufacturing Industries</li>
                    <li>14 - Food Service Industry</li>
                </ul>
                <br>
                <p>02 Employment Fields Covered by Specified Skilled Worker (iI)</p>
                <ul class="offset-4">
                    <li>15 - Construction Industry (Managing the work of constructing houses, buildings, and other structures)</li>
                    <li>16 - Ship building and Ship MAchinery Industries (Managing the work of Building Ships)</li>
                </ul>
                <br>
                <p>Technical Intern Training (TITP) is a status of residence for receiving training, but Specified Skilled Worker is a status of residence for working.</p>
                <br>
                <p>The Specified Skilled Worker (i) Status of Residence</p>
                <br>
                <p>You can work in Japan for a total of five years.</p>
                <p>You cannot bring your family to Japan.</p>
                <p>You receive the same salary as Japanese workers.</p>
                <p>You receive Japanese language training.</p>
                <p>You receive various forms of support from your company.</p>
                <p>The Specified Skilled Worker (ii) Status of Residence</p>
                <br>
                <p>No duration restrictions</p>
                <p>You can bring your family to Japan.</p>
